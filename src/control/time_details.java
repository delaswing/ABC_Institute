
package control;


public class time_details {
    private int id;
    private int no_date;
    private int time_hour;
    private int time_min;
    private int l_id;


    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the no_date
     */
    public int getNo_date() {
        return no_date;
    }

    /**
     * @param no_date the no_date to set
     */
    public void setNo_date(int no_date) {
        this.no_date = no_date;
    }

    /**
     * @return the time_hour
     */
    public int getTime_hour() {
        return time_hour;
    }

    /**
     * @param time_hour the time_hour to set
     */
    public void setTime_hour(int time_hour) {
        this.time_hour = time_hour;
    }

    /**
     * @return the time_min
     */
    public int getTime_min() {
        return time_min;
    }

    /**
     * @param time_min the time_min to set
     */
    public void setTime_min(int time_min) {
        this.time_min = time_min;
    }

    /**
     * @return the l_id
     */
    public int getL_id() {
        return l_id;
    }

    /**
     * @param l_id the l_id to set
     */
    public void setL_id(int l_id) {
        this.l_id = l_id;
    }
    
    
}
